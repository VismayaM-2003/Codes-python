#Loop tubles
#Loop through the tuple items by using a for loop.
one=("Mobile","Laptop","Computer","Tab")
for two in one:
    print(two)
print("*********************************************************")

#Loop Through the Index Numbers---range() and len() functions to create a suitable iterable
task=("int","str","Char","float")
for i in range(len(task)):
    print(task[i])
print("*****************************************************************************************")

#while loop
zero=(1,2,3,4,5,6,7,8)
i = 0
while i < len(zero):
  print(zero[i])
  i = i + 1
print("**************************************************************************")


