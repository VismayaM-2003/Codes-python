#Tuples-used to assign multiple items in one variable,it is a collection(ordered,unchangeable,allow duplicate values)
#written with round brackets.
values=("Blue","Orange","Red","Blue","Yellow","Red","Violet")#allow duplicate values
print(values)
print("**************************************************************************************")

#Tuple Length
items=("Carrot","Halwa","Apple","Banana","Watermelon")#length of the tuple(5)
print(len(items))
print("******************************************************************************************")

#Create Tuple With One Item add (,) aftr the item
one=("Table",)
print(type(one)) #type is tuple
two=("Board")
print(type(two)) #type is str
print("*******************************************************************************************")

#Tuple items-DataTypes
one=("Mall","Beach","Cinema","School","College")#str
two=(1,2,3,4,5,6)                               #int
three=(True,False,True,True,False)              #boolean
print(one)
print(two)
print(three)
numbers=("ABC",345,True,78.09,"Female")#Can contain different data types
print(numbers)
print("**********************************************************************************************")

#Type()
four=(12,34,2,30,8)
print(type(four))
